const MONTH_NAMES = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

export function getMonthName(month: number): string {
  return MONTH_NAMES[month - 1] ?? '';
}

export function findCityClimate(query: string): CityClimate | null {
  const q = query.toLowerCase().trim();
  if (!q) return null;

  // Exact match on city name
  for (const entry of climateData) {
    if (entry.city.toLowerCase() === q) return entry;
  }

  // Exact match on alias
  for (const entry of climateData) {
    if (entry.aliases?.some((a) => a.toLowerCase() === q)) return entry;
  }

  // City name starts with query or query starts with city name
  for (const entry of climateData) {
    const cityLower = entry.city.toLowerCase();
    if (cityLower.startsWith(q) || q.startsWith(cityLower)) return entry;
  }

  // Partial match: query contains city name or city name contains query (min 3 chars)
  if (q.length >= 3) {
    for (const entry of climateData) {
      const cityLower = entry.city.toLowerCase();
      if (q.includes(cityLower) || cityLower.includes(q)) return entry;
    }

    // Partial match on aliases
    for (const entry of climateData) {
      if (entry.aliases?.some((a) => {
        const aLower = a.toLowerCase();
        return q.includes(aLower) || aLower.includes(q);
      })) return entry;
    }
  }

  return null;
}
